namespace Weave.Core.Enums;

/// <summary>
///     The fundamental thread types that can be manipulated.
/// </summary>
public enum ThreadType : byte
{
    Force = 1,       // Electromagnetic, gravitational, and nuclear forces
    Matter = 2,      // Atomic and molecular structure
    Probability = 3, // Likelihood of events occurring
    Spacetime = 4    // Flow of time and spatial relationships
}