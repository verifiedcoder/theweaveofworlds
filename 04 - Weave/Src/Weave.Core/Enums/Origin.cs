namespace Weave.Core.Enums;

/// <summary>
///     Character origins that determine starting bonuses.
/// </summary>
public enum Origin : byte
{
    Academic = 0,  // University-trained, strong theoretical foundation
    Intuitive = 1, // Self-taught, developed abilities naturally
    Military = 2,  // Combat-trained, focused on practical applications
    Hermetic = 3   // Isolated, developed unique approaches
}