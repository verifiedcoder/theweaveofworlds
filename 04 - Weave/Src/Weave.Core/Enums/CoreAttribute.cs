namespace Weave.Core.Enums;

/// <summary>
///     The five core attributes that define a character's capabilities.
/// </summary>
public enum CoreAttribute : byte
{
    Coherence = 0,  // Mental stability and focus
    Insight = 1,    // Ability to perceive thread patterns
    Resonance = 2,  // Natural harmony with quantum fabric
    Volatility = 3, // Capacity for power and intensity
    Cognition = 4   // Scientific knowledge and understanding
}